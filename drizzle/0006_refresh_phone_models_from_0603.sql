INSERT INTO "phone_models" (
	"model",
	"base_price",
	"grade_a_percent",
	"grade_b_percent",
	"grade_c_percent",
	"grade_d_percent"
)
SELECT
	"model",
	"base_price",
	"grade_a_percent",
	"grade_b_percent",
	"grade_c_percent",
	"grade_d_percent"
FROM "phone_models_refresh_0603"
ON CONFLICT ("model") DO UPDATE
SET
	"base_price" = excluded."base_price",
	"grade_a_percent" = excluded."grade_a_percent",
	"grade_b_percent" = excluded."grade_b_percent",
	"grade_c_percent" = excluded."grade_c_percent",
	"grade_d_percent" = excluded."grade_d_percent",
	"updated_at" = now();

DELETE FROM "phone_models" AS "pm"
WHERE NOT EXISTS (
	SELECT 1
	FROM "phone_models_refresh_0603" AS "src"
	WHERE "src"."model" = "pm"."model"
)
AND NOT EXISTS (
	SELECT 1
	FROM "valuations" AS "v"
	WHERE "v"."phone_model_id" = "pm"."id"
);

SELECT setval(
	pg_get_serial_sequence('phone_models', 'id'),
	(SELECT COALESCE(MAX("id"), 1) FROM "phone_models")
);
